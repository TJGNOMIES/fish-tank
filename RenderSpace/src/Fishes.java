public class Fishes {
    public int xpos;
    public int ypos;
    public int width;
    public int height;
    public int dx;
    public int dy;


    public Fishes(int pWidth, int pHeight, int pXpos, int pYpos, int pDx, int pDy) {
        width = pWidth;
        height = pHeight;
        xpos = pXpos;
        ypos = pYpos;
        dx = pDx;
        dy = pDy;
    }
}
